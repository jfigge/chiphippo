/**
 * The 8 dot-rows for a character code. Codes 0x00–0x0F show the 8 CGRAM glyphs
 * (0x08–0x0F alias 0x00–0x07); everything else comes from the ROM table, with
 * an unmapped code rendering blank.
 * @param {number} code   0–255 DDRAM byte
 * @param {Uint8Array} [cgram]  64-byte custom-glyph RAM (8 glyphs × 8 rows)
 * @returns {Uint8Array} eight 5-bit rows
 */
export function glyphRows(code, cgram) {
  if (code < 0x10) {
    const base = (code & 0x07) * 8;
    if (cgram && cgram.length >= base + 8)
      return cgram.subarray(base, base + 8);
    return BLANK;
  }
  return CGROM_A00[code] ?? BLANK;
}
