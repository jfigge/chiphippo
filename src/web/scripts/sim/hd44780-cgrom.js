// hd44780-cgrom.js — the HD44780 character-generator font as PURE DATA (no
// DOM). Each glyph is a Uint8Array(8): one byte per dot-row, the low 5 bits
// being the 5 dot columns with bit 4 (0b10000) the LEFTMOST column. This is
// the same row/bit convention as the HD44780's CGRAM, so custom characters and
// ROM glyphs render through one path (`glyphRows`).
//
// Scope: the printable ASCII subset 0x20–0x7E (the A00 ROM matches ASCII here
// except for a few Japanese substitutions — ¥ at 0x5C, → / ← at 0x7E/0x7F —
// which we render as the ASCII glyphs for legibility). The kana/symbol banks
// (0xA0–0xFF) are not included; an unmapped code renders blank rather than
// crashing. Character codes 0x00–0x0F display the 8 CGRAM glyphs (0x08–0x0F
// alias 0x00–0x07), which come from run-volatile state, not this table.

/** Build a glyph from eight 5-bit row values. */
